public class P5_CumulativeProduct {

	public static void main(String[] args) {
		int NumberofVariables = 3;
		int x = 5;
		int y = 2;
		int z = 4;
		
		System.out.println("How many numbers will you multiply? " + NumberofVariables);
		System.out.println("Enter number 1: " + x);
		System.out.println("Enter number 2: " + y);
		System.out.println("Enter number 3: " + z);
		System.out.println("The final product is " + (x * y * z) + ".");
		
	}

}
